#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;
	size_t	i;

	i = 0;
	if (n != 0)
	{
		tmp = s;
		while (i < n)
		{
			tmp[i] = '\0';
			i++;
		}
		s = tmp;
	}
}
